    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Document documento = new Document();
        
        try {
            PdfWriter.getInstance(documento, new FileOutputStream("C:/Users/frank/OneDrive/Escritorio/" + "Listado de Turnos.pdf"));
            documento.open();
            
            PdfPTable tabla = new PdfPTable(4);
            
            tabla.addCell("ID");
            tabla.addCell("Nombre y Apellido");
            tabla.addCell("Fecha del Turno");
            tabla.addCell("Hora");
            
            try {
                Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/turno_bd", "root", "");
                PreparedStatement pst = cn.prepareStatement("SELECT * FROM turnos");
                
                ResultSet rs= pst.executeQuery();
                
                if (rs.next()) {
                    
                    do {     
                        tabla.addCell(rs.getString(1));
                        tabla.addCell(rs.getString(2));
                        tabla.addCell(rs.getString(3));
                        tabla.addCell(rs.getString(4));
                        
                    } while (rs.next());
                    documento.add(tabla);
                }
                
            } catch (DocumentException | SQLException e) {
                    e.printStackTrace();
            }
            documento.close();
            JOptionPane.showMessageDialog(null, "Listado Creado y Descargado");
        } catch (DocumentException | HeadlessException | FileNotFoundException e) {
        }
    }//GEN-LAST:event_jButton3ActionPerformed

