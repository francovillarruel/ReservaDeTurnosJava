    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/turno_bd", "root", "");
            cn.setAutoCommit(false); // Desactiva el autocommit
            PreparedStatement pst = cn.prepareStatement("select * from turnos where nombre = ?");

            pst.setString(1, txt_buscar.getText().trim());

            ResultSet rs = pst.executeQuery();
            lavel_txt.setText("");

            if (rs.next()) {
                label_fecha.setText("Turno para la fecha: " + rs.getString("fecha"));
                label_hora.setText("Turno para la fecha: " + rs.getString("hora"));
                txt_nya.setText(rs.getString("nombre"));
                txt_fecha.setText(rs.getString("fecha"));
                txt_hora.setText(rs.getString("hora"));
                label_sinturno.setText("");
            } else {
                label_sinturno.setText("Cliente sin Turno asignado ");
            }


        } catch (SQLException e) {
            e.printStackTrace();
            System.out.println("SQL State: " + e.getSQLState());
            System.out.println("Error Code: " + e.getErrorCode());
            lavel_txt.setText("Error al asignar el turno.");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

